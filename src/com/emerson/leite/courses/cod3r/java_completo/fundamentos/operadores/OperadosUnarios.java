package com.emerson.leite.courses.cod3r.java_completo.fundamentos.operadores;

public class OperadosUnarios {

    public static void main(String[] args) {


        int a  =1 ;
        int b = 2;

        a++; // a = a + 1;
        a--; // a = a - 1;

        ++b; // b = b + 1;
        --b; // b = b - 1;

        System.out.println(++a == b--); // 2 == 2 , b é incrementado depois da comparação e a antes da comparação

    }




}
