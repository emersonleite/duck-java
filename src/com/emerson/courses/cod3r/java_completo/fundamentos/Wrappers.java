package com.emerson.courses.cod3r.java_completo.fundamentos;

public class Wrappers {

    public static void main(String[] args) {

        // byte
        Byte b = 100;

        // short
        Short s = 1000;

        // int
        Integer i = 10000;

        // long
        Long l = 1000000L;

        // float
        Float f = 123.10F;

        // double
        Double d = 12752.5678;

        // boolean
        Boolean bo = true;
        Boolean bo2 = Boolean.parseBoolean("true");

        System.out.println(bo2);
        System.out.println(bo2.toString().toUpperCase());

        // char
        Character c = 'T';


    }

}
